package Project__Students_and_Grades;

public class Students {
    private String name;
    private double grade;

    //constructor
    public Students(String name, double grade){
        this.name = name;
        this.grade = grade;
    }
    //getters
    public String getName(){
        return name;
    }
    public double getGrade(){
        return grade;
    }
    //setters

}
